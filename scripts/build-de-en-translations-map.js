EN_TO_DE.sort((a, b) => b[0].length - a[0].length);
ES_TO_DE.sort((a, b) => b[0].length - a[0].length);

function applyReplacements(text, pairs) {
  let out = text;
  for (const [from, to] of pairs) {
    out = out.split(from).join(to);
  }
  return out;
}

function translateEn(en) {
  return applyReplacements(en, EN_TO_DE);
}

function esToDe(es) {
  return applyReplacements(es, ES_TO_DE);
}

function main() {
  const esDict = loadEsDict();
  const supplemental = loadSupplemental();
  const out = { ...EN_OVERRIDES, ...supplemental };

  for (const [en, es] of Object.entries(esDict)) {
    if (Object.prototype.hasOwnProperty.call(out, en)) continue;
    const fromEn = translateEn(en);
    const fromEs = esToDe(es);
    out[en] = fromEn !== en ? fromEn : fromEs;
  }

  // Fix a few known bad supplemental entries
  if (out.Menu === 'Speisekarte') out.Menu = 'Menü';
  if (out.Cancel === 'Stornieren') out.Cancel = 'Abbrechen';
  if (out['Image of course in %s'] && out['Image of course in %s'].includes('natürlich')) {
    out['Image of course in %s'] = 'Bild des Kurses in %s';
  }

  const DE_OVERRIDES = {
    'What is cleaned up:': 'Was wird bereinigt:',
    'Added directly via WordPress': 'Direkt über WordPress hinzugefügt',
    'Kursagenten plugin - Version history': 'Kursagenten-Plugin – Versionsverlauf',
    'Valid regions: <code>sørlandet</code>, <code>østlandet</code>, <code>vestlandet</code>, <code>midt-norge</code>, <code>nord-norge</code>':
      'Gültige Regionen: <code>sørlandet</code>, <code>østlandet</code>, <code>vestlandet</code>, <code>midt-norge</code>, <code>nord-norge</code>',
    '"Slug" is the SEO-friendly version of the URL. Example /kari-norman':
      '„Slug“ ist die SEO-freundliche Version der URL. Beispiel /kari-norman',
    '"Slug" is the SEO-friendly version of the URL. Example /my-course':
      '„Slug“ ist die SEO-freundliche Version der URL. Beispiel /my-course',
    '"Slug" is the SEO-friendly version of the URL. Example /oslo':
      '„Slug“ ist die SEO-freundliche Version der URL. Beispiel /oslo',
  };
  Object.assign(out, DE_OVERRIDES);

  const keys = Object.keys(esDict);
  const missing = keys.filter((k) => !Object.prototype.hasOwnProperty.call(out, k));
  if (missing.length) {
    console.error('Missing keys:', missing.length);
    process.exit(1);
  }

  fs.writeFileSync(OUT_PATH, `${JSON.stringify(out, null, 2)}\n`);
  const stillEn = keys.filter((k) => out[k] === k);
  console.log(`Wrote ${OUT_PATH} (${keys.length} entries)`);
  console.log(`Still identical to English: ${stillEn.length}`);
}

main();
