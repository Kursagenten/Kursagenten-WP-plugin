/**
 * Build lang/de-en-translations.json (English -> German) from es-dict keys.
 * Uses supplemental translations plus es-dict Spanish as a translation scaffold.
 */
const fs = require('fs');
const path = require('path');

const LANG_DIR = path.join(__dirname, '..', 'lang');
const ES_DICT_DIR = path.join(LANG_DIR, 'es-dict');
const OUT_PATH = path.join(LANG_DIR, 'de-en-translations.json');

function loadEsDict() {
  const dict = {};
  for (let i = 1; i <= 7; i++) {
    Object.assign(
      dict,
      JSON.parse(fs.readFileSync(path.join(ES_DICT_DIR, `chunk-${i}.json`), 'utf8'))
    );
  }
  return dict;
}

function loadSupplemental() {
  const map = {};
  const supplementalPath = path.join(__dirname, '_pl-missing-with-de.json');
  if (!fs.existsSync(supplementalPath)) return map;
  const supplemental = JSON.parse(fs.readFileSync(supplementalPath, 'utf8'));
  for (const entry of supplemental) {
    if (entry.en && entry.de) {
      map[entry.en] = entry.de;
    }
  }
  return map;
}

